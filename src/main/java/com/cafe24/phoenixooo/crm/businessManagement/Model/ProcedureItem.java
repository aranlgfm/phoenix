package com.cafe24.phoenixooo.crm.businessManagement.Model;

public class ProcedureItem {
	private String itemCode;
	private String shopCode;
	private String itemName;
	private String itemRegisteDate;
	
	
	public String getItemRegisteDate() {
		return itemRegisteDate;
	}
	public void setItemRegisteDate(String itemRegisteDate) {
		this.itemRegisteDate = itemRegisteDate;
	}
	public String getItemCode() {
		return itemCode;
	}
	public void setItemCode(String itemCode) {
		this.itemCode = itemCode;
	}
	public String getShopCode() {
		return shopCode;
	}
	public void setShopCode(String shopCode) {
		this.shopCode = shopCode;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
}
