package com.cafe24.phoenixooo.crm.businessManagement.Model;

public class ProcedureItemDesign extends ProcedureItem{
	
	private String itemDesignCode;
	private String itemDesignName;
	private int itemDesignPrice;
	private String itemDesignRegisteDate;
	
	public String getItemDesignCode() {
		return itemDesignCode;
	}
	public void setItemDesignCode(String itemDesignCode) {
		this.itemDesignCode = itemDesignCode;
	}
	public String getItemDesignName() {
		return itemDesignName;
	}
	public void setItemDesignName(String itemDesignName) {
		this.itemDesignName = itemDesignName;
	}
	public int getItemDesignPrice() {
		return itemDesignPrice;
	}
	public void setItemDesignPrice(int itemDesignPrice) {
		this.itemDesignPrice = itemDesignPrice;
	}
	public String getItemDesignRegisteDate() {
		return itemDesignRegisteDate;
	}
	public void setItemDesignRegisteDate(String itemDesignRegisteDate) {
		this.itemDesignRegisteDate = itemDesignRegisteDate;
	}
	
	
	
	
}
